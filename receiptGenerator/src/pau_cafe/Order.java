    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        try{
        DefaultTableModel td = (DefaultTableModel)jTable1.getModel();
        String receipt = jLabel9.getText() + "\t" + jLabel10.getText() + "\n\n\n\t\tYour Order\n=================================================================\n"
                + "S/N\tFoodItem\t\tUnit Price\tQuantity\tTotal Price\n______________________________________________________________________________\n";
        for (int d = 0; d < td.getRowCount(); d++){
            for (int e = 0; e < 5; e++){
            receipt += td.getValueAt(d, e);
            if(e == 1){
            String check = td.getValueAt(d, e).toString();
            if (check.length() > 13){
                receipt += "\t";
            }else{
            receipt += "\t\t";
            }
            }
            else{
            receipt += "\t";
            }
            }
            receipt += "\n";
            }
        
        receipt += "\n\t\t\t\t              Total: " + jLabel5.getText();
        jTextArea1.setText(receipt);
        jTextArea1.print();
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Failed to generate receipt");
        
        }
    }//GEN-LAST:event_jButton5ActionPerformed


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
        String name = jLabel4.getText();
        float price = Float.parseFloat(jLabel7.getText());
        int quantity = Integer.parseInt(jTextField1.getText());
        float total = price * quantity;
         
        
        Object addrow[] = {sn, name, price, quantity, total};
       
            DefaultTableModel td = (DefaultTableModel)jTable1.getModel();
            td.addRow(addrow);
            int renumber = td.getRowCount();
            for (int b = 0; b < renumber; b++){
                td.setValueAt(b+1, b, 0);
            };
            sum = 0;
            for (int c = 0; c < renumber; c++){
                sum += Float.parseFloat(td.getValueAt(c, 4).toString());               
            }
            jTextField1.setText("");
            jLabel4.setText("");
            jLabel7.setText("");
            jLabel5.setText(String.valueOf(sum));
        }
        catch(Exception e){
             if ((foodItem.equals("") && price.equals("")) ||  quantity == null){
             JOptionPane.showMessageDialog(rootPane, "Order details not completed");
             }
            
        };
      
    }//GEN-LAST:event_jButton1ActionPerformed

